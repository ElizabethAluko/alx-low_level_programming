#include "3-calc.h"

/**
 * op_add - sum two numbers.
 * @a: first int number.
 * @b: second int number.
 * Return: sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract one number from other
 * @a: first int number.
 * @b: second int number.
 * Return: a minus b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply one number by other
 * @a: first int number.
 * @b: second int number.
 * Return: a multiply by b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide one number by other
 * @a: first int number.
 * @b: second int number.
 * Return: a divided by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - remainder from division of one number by other
 * @a: first int number.
 * @b: second int number.
 * Return: a module b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
